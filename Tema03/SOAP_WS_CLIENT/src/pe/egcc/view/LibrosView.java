    private void btnLibrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLibrosActionPerformed
        LibroService service = new LibroService();
        List<Libro> lista = service.traerLibros();
        DefaultTableModel tabla;
        tabla = (DefaultTableModel) tblData.getModel();
        tabla.setRowCount(0);
        for (Libro l : lista) {
            Object[] rowData = {l.getAnio(),
            l.getAutores().stream().map(Object::toString)
                        .collect(Collectors.joining(", ")), 
            l.getEditorial(), l.getTitulo(), l.getResumen() };
            tabla.addRow(rowData);
        }
    }//GEN-LAST:event_btnLibrosActionPerformed

